# lambda function to create area of square
area_square = lambda s: s*s
print (f"The area of square whose side is 5 cm is {area_square(5)} cm^2")

# regular function to create area of square
def areaSquare(s):
    return s* s
print (f"The area of square whose side is 5 cm is {areaSquare(5)} cm^2")

add = lambda x, y: x + y
print(f"4+5 is {add(4, 5)}")